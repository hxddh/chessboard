/**
 * Generate src/web/js/engine-src.js: the vendored Stockfish loader text plus
 * the wasm binary as base64, exposed as globals for engine.js to build its
 * Blob worker from. Needed because the zero:// scheme cannot serve worker
 * scripts or fetch packaged files (proven in the goban project) — everything
 * the worker needs must arrive via <script> + postMessage.
 *
 * Run: node scripts/gen-engine-src.mjs   (output is generated at build time,
 * gitignored — third_party/stockfish/ is the committed source of truth)
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const SF = path.join(root, "third_party", "stockfish");

const loader = fs.readFileSync(path.join(SF, "stockfish-18-lite-single.js"), "utf8");
const wasm = fs.readFileSync(path.join(SF, "stockfish-18-lite-single.wasm"));

const out =
  "/* generated by scripts/gen-engine-src.mjs — do not edit.\n" +
  " * Stockfish.js 18 lite-single (GPLv3) — see third_party/stockfish/. */\n" +
  "window.CHESS_SF_LOADER = " + JSON.stringify(loader) + ";\n" +
  "window.CHESS_SF_WASM_B64 = " + JSON.stringify(wasm.toString("base64")) + ";\n";

const dest = path.join(root, "src", "web", "js", "engine-src.js");
fs.writeFileSync(dest, out);
console.log("wrote", dest, Math.round(out.length / 1024 / 1024 * 10) / 10 + "MB");
